public class Escape_sq {
    public static void main(String[] args){
        // Escape Sequences in Java

        // \t use for Tab
        // \n use for next Line
        // \b use for backspace
        // \' use for single quote
        // \" use for double quote
        // \\ use for backslash

        System.out.println("Hey this is \tSagar");
        System.out.println("Hey this is \nSagar");
        System.out.println("Hey this\b is Sagar");
        System.out.println("Hey this is \'Sagar\' ");
        System.out.println("Hey this is \"Sagar\" ");
        System.out.println("Hey this is \\Sagar\\");

    }
}
